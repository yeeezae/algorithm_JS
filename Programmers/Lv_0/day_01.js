//변수의 이해
//Solution 함수 이해

//01. 두수의 합
function solution(num1,num2) {
  return num1 + num2
}

//02. 두수의 차
function solution(num1,num2) {
  var answer = num1 - num2
  return answer
}

//03. 두수의 곱
function solution(num1,num2) {
  var answer = num1 * num2
  return answer
}

//04. 두수의 몫
//Math.floor를 이용해 버림을 사용할 수 있다.
function solution(num1,num2) {
  var answer = Math.floor(num1 / num2)
  return answer
}